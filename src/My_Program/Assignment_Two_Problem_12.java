package My_Program;

public class Assignment_Two_Problem_12 {
	public static void main(String[] args) {
		/*
		 Q..Write a program to draw 
		 following star pattern 
    
    *  *  *  * 
    *  *  *  * 
    *  *  *  *
		 */
		
		for (int i=1; i<=3; i++)
		{
			for(int j=1; j<=4;j++)
			{
				System.out.print(" *");
			}
			System.out.println();
		}
	}

}
